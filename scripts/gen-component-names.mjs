// Generates src/component-names.ts from the actual component files, so the Nuxt
// auto-registration module (and anyone listing components) never drifts when a
// new Lp* component is added. Run as part of the build.
import { readdirSync, writeFileSync } from "node:fs"
import { dirname, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const componentsDir = resolve(here, "../src/components")
const out = resolve(here, "../src/component-names.ts")

const names = readdirSync(componentsDir)
  .filter((f) => f.endsWith(".vue"))
  .map((f) => f.replace(/\.vue$/, ""))
  .sort()

const body = `/* AUTO-GENERATED by scripts/gen-component-names.mjs — do not edit.
 * The full list of kit component names, kept in sync with the component files
 * so the Nuxt module (./nuxt) auto-registers every component including new ones. */
export const COMPONENT_NAMES = ${JSON.stringify(names, null, 2)} as const
`

writeFileSync(out, body)
console.log(`gen-component-names: wrote ${names.length} names`)
